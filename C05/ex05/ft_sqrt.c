int	ft_sqrt(int nb)
{
	int	i;
	int	dm;

	i = 0;
	dm = nb / 2;
	if (nb == 1)
		return (1);
	if (nb != 0)
	{
		while (i <= dm)
		{
			if (nb == i * i)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
